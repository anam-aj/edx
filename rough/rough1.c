#include <cs50.h>
#include <ctype.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main(void)
{
    char a = 'p';
    printf("%c\n", a);

    a = 'q';
    printf("%c\n", a);

    char p = 'x';
    char q = p;
    printf("%c\n", q);

    q = 'z';

    printf("%c\n", p);
    printf("%c\n", q);




}
